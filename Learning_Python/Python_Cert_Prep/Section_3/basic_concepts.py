# Bit operators
# &
first_bit = 1
second_bit = 1

print(first_bit & second_bit)

# |
first_bit = 1
second_bit = 0

print(first_bit | second_bit)

# ^
first_bit = 0
second_bit = 0

print(first_bit ^ second_bit)

# ~
print(~0)
